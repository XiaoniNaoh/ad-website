<h3>7.高强度间歇训练法</h3>
<p>高强度间歇训练法对那些有一些锻炼功底，想要改变锻炼模式，或是没有锻炼时间的人说相当不错。HIIT是密集的、高频率的短时爆发力训练，两组高强度训练中间穿插一小段休息和恢复时段。HIIT的锻炼方式旨在在较短时间内将你的肌肉与心肺功能推到最高负荷。典型的HIIT训练流程，用时是非常短的，通常连30分钟都不到。当然，训练时长也要根据你的训练水平来定。从健康收益角度来看，HIIT能在更短的时间里，达到和传统锻炼方式相同的效果，比如减肥，降低静息心率与血压。另外，比起传统形式的锻炼，HIIT在降血糖与增加胰岛素敏感度方面效果更好。最重要的是， HIIT已被证明能够提升老年人群的认知能力，提升幅度最大的是变速处理能力，其次是记忆与执行功能。</p>

<h3>8.氧气治疗呼吸训练</h3>
<p>这项锻炼对于那些有心脑血管病史者，或高危人群而言尤其具有帮助作用。EWOT训练有很多功效，最大的功效在于能够改善脑部以及周围神经系统的循环功能”)。EWOT训练时使用的供氧面具是特别定制的，它至少能够提供8~10 L/min流速的纯氧气体(纯度90%~95%)，以支持你进行一定时长的锻炼。鉴于人们不可能背着供氧设备做室外运动，因此氧疗运动只适合固定的跑步机，或是固定单车(动感单车)。我们认为，每周3次，每次15分钟的氧疗运动是比较合理的，当然这也要根据你自身的情况来定。</p>

<h3>9.采取综合训练方式</h3>
<p>虽然走步锻炼上面介绍了很多，但说实话，没必要将锻炼形式单单拘泥于每天走步。锻炼在于乐趣，锻炼在于新鲜!此时，交叉训练法就极为重要了，即通过切换锻炼动作来达到激活身体各个肌群的效果。基于这个理念，你可以考虑一下加入附近的健身房、YMCAO，或是社区中心举办的力量训练团体课。与一个能满足你锻炼目标的训练者一起锻炼也不失为一个好主意。组团锻炼往往更有趣，气氛更活跃，团队协作的紧密联系对认知健康也更为有益。</p>
<p>可供选择的体育项目有游泳、拳击、打乒乓球、匹克球与尊巴健身操等等。不同的环境有不同的选择，如生活在北方严寒地区，雪鞋行走是一项极佳的锻炼；如果居住在傍水之地，划皮划艇也是个不错的选择。</p>

<h3>>10.克服身体限制</h3>
<p>瑜伽与普拉提对于那些暂时或长期身体行动不便的人来说尤其有益，它们的大多数动作都是在地板上铺着的软垫上完成的。因此，足部、脚踝、膝盖或臀部的问题与旧伤等不太会影响瑜伽与普拉提的这些运动。</p>
<p>气功和太极拳则是相当不错的慢节奏、静态发力的锻炼手段。</p>
<p>坐姿健身课程对于正处于运动损伤恢复期，或是行动能力受限的人来说，也不失为一种可能的锻炼手段。坐姿健身课程的动作，都是专门设计用来进行椅上锻炼的，可以在有限的活动范围内做出优美、得体的动作。一般这种坐姿健身课程都会配备健身教练。如果你因为伤病等原因，无法顺利完成预设的动作，健身教练也可以根据你的情况对动作进行定制改变。</p>


        
