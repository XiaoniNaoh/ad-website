<h3>1.去户外运动</h3>
<p>有研究显示，花时间贴近大自然有益于你的大脑和整体健康。进行户外运动被证实能够减少压力，增强创造性和提升解决问题的能力，能使你精神更加集中，并且将负面情绪最小化。户外运动(尤其是早晨进行)还有一个很重要的好处，就是可以让你的眼睛接收到更多阳光的照射，有助于维持健康的昼夜节律，从而睡得更好!</p>

<h3>2.增加走步锻炼</h3>
<p>走步是最简单的有氧运动，一定程度上也是一种力量训练——可以把走步想象成携带自身重量作平面运动。行走锻炼是带着目标的训练，而不是随意闲适的乱晃。行走时，可以想象自己马上就要迟到，以这样的节奏强度来行走。当然，走步锻炼也要根据自身的身体情况来调整，你可能一开始的时候强度和速度都不行，慢一点也不要紧。不过，对于走步这种强度不是很大的运动，你最好每天都能进步一点点——每天都要比昨天坚持多走几分钟，直到你能一次性走步30分钟以上。</p>
<p>以下是充分利用走步锻炼时间的一点小建议：</p>
<p>·和朋友一起走步</p>
<p>·提高走步速度</p>
<p>·配合音乐锻炼</p>
<p>·健身的同时健脑：将健身训练纳入每天的走步计划，走步的同时可以尝试倒背英文字母表</p>
<p>·燃烧脂肪的同时进行学习</p>
<p>·使用负重背心</p>
<p>·加上弓步行走动作</p>
<p>·养只宠物狗作为陪伴</p>
<p>·用电子设备等跟踪锻炼进度</p>

<h3>3.利用弹力带进行徒手力量训练</h3>
<p>弹力带(或称为阻力带)是一种徒手力量锻炼(不用在家买一大堆健身器材或是去健身房办卡)的极佳方式。弹力带很便宜，且轻巧便携，不用时可收纳起来，尤其适合那些需经常出差的人。从本质上说，弹力带就是一根粗大的橡胶制带状物,被加工成多种规格，每种规格提供的阻力不尽相同，以此来迎合不同水平的健身人士。弹力带的使用方式也不尽相同，既可以用来模拟器械训练，也可以用来辅助自重训练。</p>

<h3>4.充分驾驭自身的身心链接</h3>
<p>瑜伽与普拉提都能舒解压力，提升身体的柔韧性与平衡性，增强整体力量。瑜伽侧重于身体柔韧性以及大肌群的锻炼，并且带有一些精神元素;而普拉提则更多的着力于身体控制、肌肉的美观和核心力量训练。当然，无论哪项运动都需要强力的心身链接，而心身链接已被研究证实可以提升认知能力，并改善其他健康参数。气功与太极也是不错的选择，它们都带有一些冥想的成分(可以舒缓压力)，同样需要强力的心身链接。一些瑜伽姿势甚至可以惊人的方式促进神经系统的保护。拉姆汗博士是一名神经科学家，也是一名经验丰富的瑜伽修炼者，他提出练习一些平缓的倒立姿势，如瑜伽中的下犬式，可以激活大脑类淋巴系统。</p>

<h3>5.进行跳跃运动</h3>
<p>一项有趣的锻炼项目是蹦床弹跳。蹦床弹跳的动作很简单，就是单纯地跳起，然后落在一个小蹦床上面。它虽然动作简单，却对健康大有裨益，其中最重要的益处就是可以激活淋巴循环系统，从而促进脑中有害物质的清除。</p>
<p>如果你担心在小蹦床上失去平衡，那么，切记要买那种带有平衡杆的小蹦床;若你存在着膀胱控制方面的问题，那么,在进行蹦床弹跳运动之前要记得将尿液排空;如感不适，最好时不时地休息一下。</p>
<p>健康与安全是蹦床跳运动的基石。因此，在开始锻炼之前，切记要保证小蹦床与地面的牢固连接。在起跳之前一定要做好充分准备，跳起时保持双脚离地十几厘米即可。蹦跳的节奏自行把握，跳快跳慢随你，但前提是要保证15分钟的稳定蹦跳锻炼时间。进行一段时间的蹦跳锻炼后，相信你的熟练度也会有相应的提高，此时你可以增加更多的“花样”，比如开合跳、高抬腿、弯腰跳跃，以及原地小跑等。</p>

<h3>6.增强腿部的肌力</h3>
<p> 一项有趣的研究发现:根据人的腿部力量，可以准确地预测认知衰老程度与整体大脑容量。从这个结论出发，若你做腿举锻炼的时候能够承受更大的重量，预示着你随后10年里认知测试得分会比一般人要高，大脑容量更大，认知衰老的进程也会更慢。</p>
<p>如果你的腿部力量比较弱，没关系，你可以先找来一把椅子，然后屁股对着椅子蹲下去，就好像你打算坐在上面那样，但是屁股不可以触碰到椅子，并保持蹲坐的姿势，能蹲坐多久就蹲坐多久。此时，你会发现你大腿正面的肌肉，即股四头肌，会有一种灼热的紧绷感。如果坚持不住了，屁股落到椅子上也不要紧(这就是为什么我们一开始要拿把椅子来练习)，随后再以同样的姿势蹲坐，把这个动作反复做5次，将其作为一组:短暂休息之后再接着进行下一组的训练。过不了多久，你的腿部肌肉就会变得更加强壮!建议每天做满3组，即15个蹲坐。</p>
